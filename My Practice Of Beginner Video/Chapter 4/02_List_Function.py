l1 = [1,8,45,9,3,15] #Random number List
print(l1) 

# List Function
l1.sort() #List ko sort kar da ga
print(l1)

# List Reverse
l1.reverse() #List k sara element ko reverse ma sort kar da ga
print(l1)

#List append
l1.append(499) #499 ko List ka end ma jor da ga
print(l1)

#List Insert
l1.insert(3,10) #3 index pa 10 ko add kar da ga
print(l1)

#List Pop
popped = l1.pop(5) #By default last element
print(popped)

# List remove
l1.remove(499) #Remove the element by it's actuall value
print(l1)